def gen_fio(i: int) -> str:
    return f"{random.choice(SURNAMES)} {random.choice(NAMES)} {random.choice(PATRONYMICS)}"

def gen_games() -> int:
    return random.randint(0, 180)

def gen_goals(games: int, position: str) -> int:
    if position == "goalkeeper":
        conceded = random.randint(0, max(0, games))
        return -conceded  # отрицательные для вратаря
    else:
        return random.randint(0, max(0, min(games, 90)))

def gen_row(i: int):
    country = random.choice(COUNTRIES)
    club = random.choice(CLUBS)
    position = random.choices(POSITIONS, weights=POS_WEIGHTS, k=1)[0]
    games = gen_games()
    goals = gen_goals(games, position)
    fio = gen_fio(i)
    return [country, fio, club, position, games, goals]

def write_dataset_csv(n: int, out_dir: str, seed: int):
    random.seed(seed)
    os.makedirs(out_dir, exist_ok=True)
    path = os.path.join(out_dir, f"dataset_{n}.csv")
    with open(path, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["country","fullName","clubName","position","counterGames","counterGoals"])
        for i in range(1, n + 1):
            w.writerow(gen_row(i))
    return path

