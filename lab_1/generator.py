## @brief Генерирует случайное ФИО футболиста.
#  @param i Порядковый номер записи.
#  @return Строка с ФИО.
def gen_fio(i: int) -> str:
    return f"{random.choice(SURNAMES)} {random.choice(NAMES)} {random.choice(PATRONYMICS)}"

## @brief Генерирует случайное количество матчей за сборную.
#  @return Целое число от 0 до 180.
def gen_games() -> int:
    return random.randint(0, 180)

## @brief Генерирует количество голов или пропущенных мячей.
#  @param games Количество сыгранных матчей.
#  @param position Позиция игрока.
#  @return Количество голов или отрицательное число пропущенных мячей для вратаря.
def gen_goals(games: int, position: str) -> int:
    if position == "goalkeeper":
        conceded = random.randint(0, max(0, games))
        return -conceded
    else:
        return random.randint(0, max(0, min(games, 90)))

## @brief Генерирует одну строку данных о футболисте.
#  @param i Порядковый номер записи.
#  @return Список значений одной строки CSV.
def gen_row(i: int):
    country = random.choice(COUNTRIES)
    club = random.choice(CLUBS)
    position = random.choices(POSITIONS, weights=POS_WEIGHTS, k=1)[0]
    games = gen_games()
    goals = gen_goals(games, position)
    fio = gen_fio(i)
    return [country, fio, club, position, games, goals]

## @brief Создает CSV-файл с набором данных заданного размера.
#  @param n Количество записей в файле.
#  @param out_dir Папка, в которую сохраняется файл.
#  @param seed Начальное значение генератора случайных чисел.
#  @return Путь к созданному CSV-файлу.
def write_dataset_csv(n: int, out_dir: str, seed: int):
    random.seed(seed)
    os.makedirs(out_dir, exist_ok=True)
    path = os.path.join(out_dir, f"dataset_{n}.csv")

    with open(path, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)

        ## @brief Запись заголовка CSV-файла.
        w.writerow(["country","fullName","clubName","position","counterGames","counterGoals"])

        ## @brief Генерация и запись строк с данными о футболистах.
        for i in range(1, n + 1):
            w.writerow(gen_row(i))

    return path

if __name__ == "__main__":
    ## @brief Папка для сохранения всех наборов данных.
    out_dir = "football_datasets"

    ## @brief Базовое значение seed для воспроизводимости результатов.
    base_seed = 42

    ## @brief Список путей ко всем созданным файлам.
    paths = []

    ## @brief Генерация CSV-файлов для всех размеров из списка SIZES.
    for idx, n in enumerate(SIZES):
        paths.append(write_dataset_csv(n, out_dir, seed=base_seed + idx))

    ## @brief Вывод информации о созданных файлах.
    print("Готово. Созданы файлы:")
    for p in paths:
        print(p)