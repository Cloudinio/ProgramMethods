## @file generator.py
#  @brief Скрипт для генерации CSV-файлов с тестовыми данными о футболистах.

import csv
import random
import os

## @brief Размеры наборов данных, для которых будут созданы CSV-файлы.
SIZES = [100, 200, 500, 1000, 2000, 5000, 10000, 20000, 30000, 50000, 70000, 100000]

## @brief Возможные страны футболистов.
COUNTRIES = ["Россия","Испания","Германия","Франция","Италия","Португалия","Бразилия","Аргентина","Англия","Нидерланды"]

