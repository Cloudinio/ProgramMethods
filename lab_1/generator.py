## @brief Возможные позиции футболистов.
POSITIONS = ["goalkeeper", "defender", "halfdefender", "attacker"]

## @brief Вероятности выбора каждой позиции.
POS_WEIGHTS = [0.12, 0.32, 0.32, 0.24]

